# Variable Implicit Declaration
tmp1 = 1
tmp2 = "Hey"
tmp3 = True
tmp4 = 1.3

print(tmp3, " is ", type(tmp3))

# Variable Explicit Declaration
exp1: int = 1
exp2: str = "Demo"
exp3: bool = False
exp4: float = 4.3

print(exp4, " is ", type(exp4))

# list
tmp5 = ["Angular", "React", "Vue"]

print(tmp5, " is ", type(tmp5))

# Dictionary
tmp6 = {"username": "admin", "password": "1234"}
print(tmp6, " is ", type(tmp6))

tmp11 = dict(username="admin", password="6558")
tmp12 = list(("C#", "Python", "Java"))

print(tmp11)
print(tmp12)
